package com.example.assignment2;

public class MenuItemOfTheDay {
	
	private String foodItem;
	private int iconID; 
	
	public MenuItemOfTheDay(String foodItem, int iconID) {
		super();
		this.foodItem = foodItem;
		this.iconID = iconID;
	}

	public String getFoodItem() {
		return foodItem;
	}

	public int getIconID() {
		return iconID;
	}
	
	
	
	
	
	

}
